 private int factorial(int numero){
     
     if(numero<0){
         JOptionPane.showMessageDialog(rootPane, "factorial q no se puede calcular");
         return -1;
     }
     if(numero==0 ||numero==1  ){
         return 1;
     }     
     int ressul = numero;
     
     for (int i=(numero-1); i >1; i--) {
         ressul*=i;
         ressul= ressul*i;
     }          
     return ressul;
     
 }
   
    
