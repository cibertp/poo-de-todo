    public boolean primo(int n) {

        if (n < 0) {
            JOptionPane.showMessageDialog(this, "ingrese un numero positivo");
            return false;
        }
        for (int i = (int) (Math.sqrt(n)); i < 10; i++) {
            if (n % i == 0) {
                return false;

            }

        }
        return true;
    }

